<h2 class='font-bold text-xl p-3'>Parents Code of Conduct</h2>
<div class="px-3">
	<p class="my-2">1. Parents must promote sportsmanship and "Kids First!"</p>
	<p class="my-2">2. Parents must refrain from the use of obscene language at all CYBL events.</p>
	<p class="my-2">
		3. Parents must refrain from initiating or excessively arguing with game officials, coaches or
		other parents. Parents, coaches, game officials and spectators must use the "24 hour" rule when
		any dispute or disagreement develops. CYBL will not overturn or review any judgment calls made
		by game officials and all rulings are final.
	</p>
	<p class="my-2">
		4. Parents will refrain from the use of tobacco products or alcohol at all CYBL events.
	</p>
	<div class="w-full border-slate-200 border-2" />
	<p class="my-2">
		Parents or spectators that do not comply with the above code will be asked to remove themselves
		from the CYBL event. If the spectator does not immediately leave the premises CYBL reserves the
		right to contact the local authorities to assist in the removal of said spectator.
	</p>

