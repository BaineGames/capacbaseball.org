<div class="p-3">
	<h2 class="text-xl font-bold">Coaches Code of Conduct</h2>
	<p class="py-2">
		Coaches are very influential on the lives of young athletes. Participants are to be taught the
		basic fundamentals of the sport and team work concept. CYBL strives to offer a professional,
		knowledgeable and fun-filled experience for all the kids in the league.
	</p>
	<div class="bg-slate-200 w-full h-2" />
	<p class='py-1'>1. Coaches must wear CYBL provided shirt at all times and act in a professional manor.</p>
	<p class='py-1'>2. Coaches must promote sportsmanship and "Kids First" at all times.</p>
	<p class='py-1'>
		3. Coaches must be fair and consistent when enforcing rules and/or discipline to team members.
	</p>
	<p class='py-1'>
		4. Coaches will refrain from the use of obscene language in front of team members or parents at
		all CYBL events.
	</p>
	<p class='py-1'>5. Coaches will refrain from the use of alcohol and tobacco products during any CYBL event.</p>
	<p class='py-1'>
		6. Coaches are responsible for the upkeep and return of equipment at year end and must notify
		the League Equipment Member of any equipment in need of repair or replacement.
	</p>
	<p class='py-1'>
		7. Coaches will be required to attend the field setup clinic, or make arrangements with the
		Director of Operations if they can"t attend.
	</p>
	<p class='py-1'>
		8. Coaches from the Home team will be responsible for the setup of the field and maintenance of
		the high traffic areas at the completion of each game.
	</p>
	<p class='py-1'>9. Coaches are encouraged to attend the CYBL approved coaches clinic.</p>
	<p class='py-1'>
		10. Coaches are encouraged to schedule a parents meeting prior to the start of the regular and
		post season.
	</p>
	<p class='py-1'>
		11. Coaches shall not leave the field until all team members have been picked up by a parent or
		guardian.
	</p>
	<p class='py-1'>
		12. Coaches are responsible for their teams" and their parents" conduct at all CYBL Events and
		will be asked by the umpire to instruct a parent to leave if they are disruptive.
	</p>
</div>
